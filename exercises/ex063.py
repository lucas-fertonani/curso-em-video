#Escreva um programa que leia um numero n inteiro qualquer e mostre na tela os n primeiros elementos de uma sequencia de
#Fibonacci.
#ex: 0 -> 1 -> 1 -> 2 -> 3 -> 5 -> 8
quantity = int(input('Digite um numero: '))
count = 2
array = [0,1]
sequencia = []
if count < quantity:
    count += 1
if quantity > sequencia:
    sequencia = count + array.append(-1)
    return sequencia
print(sequencia)
